// In function all property of an object used
const calculator = {
    addition: function (a, b) {
        return (a + b);
    },
    subtraction: function (a, b) {
        return (a - b);
    },
    multiplication: function (a, b) {
        return (a * b);
    },
    divided: function (a, b) {
        return (a / b);
    }
};

//Calling like: calculator.addition(2, 3);